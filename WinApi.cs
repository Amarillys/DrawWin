        public static List<ProcessInfo> GetRootWindowsOfProcess(int minWidth = 56, int minHeight = 30)
        {
            var allProcesses = Process.GetProcesses();
            Dictionary<IntPtr, Process> processMap = new Dictionary<IntPtr, Process>();
            List<IntPtr> rootWindows = GetChildWindows(IntPtr.Zero);
            List<ProcessInfo> dsProcRootWindows = new List<ProcessInfo>();
            foreach (IntPtr hWnd in rootWindows)
            {
                WINDOWINFO inf = new WINDOWINFO();
                GetWindowInfo(hWnd, ref inf);
                if (inf.rcWindow.Height < minHeight || inf.rcWindow.Width < minWidth)
                    continue;

                GetWindowThreadProcessId(hWnd, out uint lpdwProcessId);
                if (!processMap.ContainsKey(hWnd))
                    processMap.Add(hWnd, getProcessById(allProcesses, lpdwProcessId));
                ProcessInfo p = new ProcessInfo
                {
                    Title  = GetText(hWnd),
                    Name   = processMap[hWnd].ProcessName,
                    Pid    = lpdwProcessId,
                    Handle = hWnd,
                    Rect   = inf.rcWindow
                };
                dsProcRootWindows.Add(p);
            }
            return dsProcRootWindows;
        }

        public static List<IntPtr> GetChildWindows(IntPtr parent)
        {
            List<IntPtr> result = new List<IntPtr>();
            GCHandle listHandle = GCHandle.Alloc(result);
            try
            {
                Win32Callback childProc = new Win32Callback(EnumWindow);
                EnumChildWindows(parent, childProc, GCHandle.ToIntPtr(listHandle));
            }
            finally
            {
                if (listHandle.IsAllocated)
                    listHandle.Free();
            }
            return result;
        }

        public static bool EnumWindow(IntPtr handle, IntPtr pointer)
        {
            GCHandle gch = GCHandle.FromIntPtr(pointer);
            List<IntPtr> list = gch.Target as List<IntPtr>;
            if (list == null)
            {
                throw new InvalidCastException("GCHandle Target could not be cast as List<IntPtr>");
            }
            list.Add(handle);
            //  You can modify this to check to see if you want to cancel the operation, then return a null here
            return true;
        }

        public static string GetText(IntPtr hWnd)
        {
            // Allocate correct string length first
            int length = GetWindowTextLength(hWnd);
            StringBuilder sb = new StringBuilder(length + 1);
            GetWindowText(hWnd, sb, sb.Capacity);
            return sb.ToString();
        }

        public static Process getProcessById(Process[] ps, uint pid)
        {
            foreach (Process p in ps)
            {
                if (p.Id == (int)pid)
                {
                    return p;
                }
            }
            return ps[0];
        }

